/** Sorted alphabetically by name, ready for the full list */
export const COUNTRIES_SORTED = [...ALL_COUNTRIES].sort((a, b) =>
  a.name.localeCompare(b.name, 'es')
);

/** Pinned at top of the picker for quick access */
export const TOP_COUNTRIES = TOP_COUNTRY_CODES
  .map(code => ALL_COUNTRIES.find(c => c.code === code))
  .filter((c): c is Country => c !== undefined)
  .filter((c, i, arr) => arr.findIndex(x => x.code === c.code && x.name === c.name) === i);

export function findCountry(code: string): Country {
  return ALL_COUNTRIES.find(c => c.code === code) ?? ALL_COUNTRIES.find(c => c.code === '+34')!;
}
